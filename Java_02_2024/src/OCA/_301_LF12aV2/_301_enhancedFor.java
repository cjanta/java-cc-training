package _301_LF12aV2;

public class _301_enhancedFor {
    public static void main(String[] args) {
        // Ein Integer-Array deklarieren und initialisieren
        int[] zahlen = {1, 2, 3, 4, 5};

        // Enhanced for Loop, um durch das Array zu iterieren
        for (int zahl : zahlen) {
            // Jedes Element des Arrays ausgeben
            System.out.println("Zahl: " + zahl);
        }

        // Ein String-Array deklarieren und initialisieren
        String[] worte = {"Hallo", "Welt", "Java"};

        // Enhanced for Loop, um durch das Array zu iterieren
        for (String wort : worte) {
            // Jedes Element des Arrays ausgeben
            System.out.println("Wort: " + wort);
        }
    }
}
