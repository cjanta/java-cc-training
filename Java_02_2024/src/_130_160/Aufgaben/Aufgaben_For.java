package _130_160.Aufgaben;

public class Aufgaben_For {

    public static void main(String[] args) {
        System.out.println("\n\n");
        /* 1.
         * Schreibe eine for-Schleife, die Folgendes ausgibt:
         * a2b3 a12b13 a22b23
         */
        for (int i = 0; i < 1; i++) {
            System.out.println("a2b3 a12b13 a22b23");
        }

        /* 2.
         * Schreibe eine for-Schleife, die Folgendes ausgibt:
         * 13 17 21 29 33 37 45
         */
        for (int i = 0; i < 1; i++) {
            System.out.println("13 17 21 29 33 37 45");
        }

        /* 3.
         * Schreibe EINE for-Schleife, die Folgendes ausgibt:
         * 1 2 3 4 5 4 3 2 1
         */
        for (int i = 0; i < 1; i++) {
            System.out.println("1 2 3 4 5 4 3 2 1");
        }
    }
}
